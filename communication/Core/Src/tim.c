/*
 * Time slice polling mechanism(10ms)
 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if ((TIM1 == htim->Instance) && (ptimer))
  {
    /*10ms*/
    for (SoftTimer *pt = ptimer->pmap; pt < ptimer->pmap + ptimer->numbers; pt++)
    {
      if ((pt->enable) && pt)
      {
        if ((++pt->counter) == pt->target_counter)
        {
          pt->execute_flag = true;
          pt->counter = 0U;
        }
      }
    }
  }
}

void Timer_Handle(void)
{
  for (SoftTimer *pt = ptimer->pmap; pt < ptimer->pmap + ptimer->numbers; pt++)
  {
    if ((pt->execute_flag) && (pt->event))
    {
      pt->event();
      pt->execute_flag = false;
    }
  }
}
